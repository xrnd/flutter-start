class SavedModel {
  String _vegetables;
  String _fruits;
  String _flowers;

  String get vegetables => _vegetables;

  set vegetables(String value) {
    _vegetables = value;
  }

  String get fruits => _fruits;

  String get flowers => _flowers;

  set flowers(String value) {
    _flowers = value;
  }

  set fruits(String value) {
    _fruits = value;
  }
}
